import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const weatherRoot = path.resolve(__dirname, "..");
const outputFile = path.join(
  weatherRoot,
  "artifacts/weather-signal/src/data/brazilMunicipalities.ts"
);

const municipalityApiUrl =
  "https://servicodados.ibge.gov.br/api/v1/localidades/municipios?orderBy=nome";
const kmlZipUrl =
  "https://geoftp.ibge.gov.br/organizacao_do_territorio/estrutura_territorial/localidades/Localidades_do_Brasil/2022/Localidades_Municipios_kml.zip";

const tempDir = path.join(os.tmpdir(), "weather-ibge");
const zipPath = path.join(tempDir, "Localidades_Municipios_kml.zip");

mkdirSync(tempDir, { recursive: true });

ensureZip();

const municipalities = loadMunicipalities();
const municipalityFiles = listMunicipalityKmlFiles();
const coordinatesByCode = new Map(
  municipalityFiles.map((file) => {
    const code = extractMunicipalityCode(file);
    const seat = extractSeatCoordinates(readKmlFile(file));
    if (!seat) {
      throw new Error(`No municipal seat coordinates found for ${file}`);
    }
    return [code, seat];
  })
);

const municipalitiesWithCoordinates = municipalities.map((municipality) => {
  const code = String(municipality.id);
  const coordinates = coordinatesByCode.get(code);
  if (!coordinates) {
    throw new Error(`Missing coordinates for municipality ${code} (${municipality.nome})`);
  }

  const stateCode =
    municipality.microrregiao?.mesorregiao?.UF?.sigla ??
    municipality["regiao-imediata"]?.["regiao-intermediaria"]?.UF?.sigla ??
    "BR";
  const state =
    municipality.microrregiao?.mesorregiao?.UF?.nome ??
    municipality["regiao-imediata"]?.["regiao-intermediaria"]?.UF?.nome ??
    "Brazil";
  const region =
    municipality.microrregiao?.mesorregiao?.UF?.regiao?.nome ??
    municipality["regiao-imediata"]?.["regiao-intermediaria"]?.UF?.regiao?.nome ??
    "Brazil";

  return {
    ibgeCode: municipality.id,
    id: `${slugify(municipality.nome)}-${municipality.id}`,
    name: municipality.nome,
    state,
    stateCode,
    region,
    latitude: coordinates.latitude,
    longitude: coordinates.longitude,
    timezone: timezoneForState(stateCode)
  };
});

const source = `// Generated by scripts/generate-brazil-municipalities.mjs
// Sources:
// - ${municipalityApiUrl}
// - ${kmlZipUrl}

export interface BrazilMunicipality {
  ibgeCode: number;
  id: string;
  name: string;
  state: string;
  stateCode: string;
  region: string;
  latitude: number;
  longitude: number;
  timezone: string;
}

export const BRAZIL_MUNICIPALITIES: BrazilMunicipality[] = ${JSON.stringify(
  municipalitiesWithCoordinates,
  null,
  2
)};
`;

writeFileSync(outputFile, source);
console.log(`Wrote ${municipalitiesWithCoordinates.length} municipalities to ${outputFile}`);

function ensureZip() {
  try {
    execFileSync("test", ["-f", zipPath]);
    return;
  } catch {
    execFileSync("curl", ["-L", "-o", zipPath, kmlZipUrl], {
      stdio: "inherit"
    });
  }
}

function loadMunicipalities() {
  const raw = execFileSync("curl", ["-s", municipalityApiUrl], {
    encoding: "utf8",
    maxBuffer: 64 * 1024 * 1024
  });
  return JSON.parse(raw);
}

function listMunicipalityKmlFiles() {
  return execFileSync("unzip", ["-Z1", zipPath], {
    encoding: "utf8",
    maxBuffer: 16 * 1024 * 1024
  })
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line.endsWith(".kml"));
}

function readKmlFile(file) {
  return execFileSync("unzip", ["-p", zipPath, file], {
    encoding: "utf8",
    maxBuffer: 32 * 1024 * 1024
  });
}

function extractMunicipalityCode(file) {
  const match = file.match(/_(\d{7})_localidades_2022\.kml$/);
  if (!match) {
    throw new Error(`Unexpected municipality file name: ${file}`);
  }
  return match[1];
}

function extractSeatCoordinates(kml) {
  const placemarks = kml.match(/<Placemark[\s\S]*?<\/Placemark>/g) ?? [];
  for (const placemark of placemarks) {
    const seatType = getSimpleDataValue(placemark, "SCT_LOCALIDADE");
    if (!seatType || !/Capital Federal|Capital Estadual|Sede Municipal/.test(seatType)) {
      continue;
    }

    const latitude = Number(getSimpleDataValue(placemark, "LAT_LOCALIDADE"));
    const longitude = Number(getSimpleDataValue(placemark, "LONG_LOCALIDADE"));
    if (Number.isFinite(latitude) && Number.isFinite(longitude)) {
      return { latitude, longitude };
    }
  }

  for (const placemark of placemarks) {
    const latitude = Number(getSimpleDataValue(placemark, "LAT_LOCALIDADE"));
    const longitude = Number(getSimpleDataValue(placemark, "LONG_LOCALIDADE"));
    const localityName = getSimpleDataValue(placemark, "NM_LOCALIDADE");
    if (localityName && Number.isFinite(latitude) && Number.isFinite(longitude)) {
      return { latitude, longitude };
    }
  }

  return null;
}

function getSimpleDataValue(input, key) {
  const match = input.match(
    new RegExp(`<SimpleData name="${key}">([\\s\\S]*?)<\\/SimpleData>`)
  );
  return match?.[1]?.trim() ?? "";
}

function slugify(value) {
  return value
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function timezoneForState(stateCode) {
  const map = {
    AC: "America/Rio_Branco",
    AM: "America/Manaus",
    AP: "America/Belem",
    MS: "America/Campo_Grande",
    MT: "America/Cuiaba",
    PA: "America/Belem",
    RO: "America/Porto_Velho",
    RR: "America/Boa_Vista"
  };
  return map[stateCode] ?? "America/Sao_Paulo";
}
